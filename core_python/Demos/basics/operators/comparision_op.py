num1=30
num2=60
num3=30

#1. == (Exact equal to )
print(num1==num2)
print(num1==num3)

#2. !=(not equal to )
print(num1!=num2)
print(num1!=num3)

#3. < (less than)
print(num1<num2)
print(num1<num3)

#4. <= (less than equal to)
print(num1<=num3)
print(num1<=num2)

#5.> (greater than  )
print(num1>num2)
print(num2>num1)

#6. >=  (greater than equal to)
print(num1>=num2)